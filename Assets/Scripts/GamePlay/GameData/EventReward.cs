using UnityEngine;
using System.Collections;

public class EventReward
{
		public int firstReward;
		public int secondReward;
		public int thirdReward;

		public EventReward (int firstReward, int secondReward, int thirdReward)
		{
				this.firstReward = firstReward;
				this.secondReward = secondReward;
				this.thirdReward = thirdReward;
		}
}
